package com.company;

public class CWH_12_PS_Q4
{
    public static void main(String[] args)
    {
        // Write the following expression in a java program
        // v^2 - u^2 / 2 a s
        int v = 4;
        int u = 3;
        int a = 5;
        int s = 2;
        float f = ((v * v) - (u * u)) / (2.0F * a * s);
        System.out.println(f);
    }
}
